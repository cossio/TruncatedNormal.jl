using Base.Test

import TruncatedNormal
TN = TruncatedNormal

@testset "moments" begin
    @test TN.tnmean(100, 115) ≈ 100.00999800099926070518490239457545847490332879043
    @test TN.tnvar(50, 70) ≈ 0.00039904318680389954790992722653605933053648912703600
    @test TN.tnmean(50, 70, -2, 3) ≈ 50.171943499898757645751683644632860837133138152489
    @test TN.tnvar(50, 70, -2, 3) ≈ 0.029373438107168350377591231295634273607812172191712
    @test TN.tnmean(-100.0, 0.0, 0.0, 2.0986317998643735) ≈ -1.6744659119217125058885983754999713622460154892645
    @test TN.tnvar(-100.0, 0.0, 0.0, 2.0986317998643735) ≈ 1.6004193412141677189841357987638847137391508803335
    @test TN.tnmean(0.0, 0.9, 0.0, 0.07132755843183151) ≈ 0.056911157632522598806524588414964004271754161737065
    @test TN.tnvar(0.0, 0.9, 0.0, 0.07132755843183151) ≈ 0.0018487407287725028827020557707636415445504260892486
    @test TN.tnmean(-100.0, 100.0, 0.0, 17.185261847875548) ≈ 0
    @test TN.tnvar(-100.0, 100.0, 0.0, 17.185261847875548) ≈ 295.33316389955773548630284123712450743144575630425
    @test TN.tnmean(-100.0, 0.5, 0.0, 0.47383322897860064) ≈ -0.12679813305217914936351767367432833143990653866916
    @test TN.tnvar(-100.0, 0.5, 0.0, 0.47383322897860064) ≈ 0.14504109581267928383732856154725101922961255730315
    @test TN.tnmean(-100.0, 100.0, 0.0, 17.185261847875548) ≈ 0
    @test TN.tnvar(-100.0, 100.0, 0.0, 17.185261847875548) ≈ 295.33316389955773548630284123712450743144575630425
end
